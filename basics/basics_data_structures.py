# DATA STRUCTURES #
###################

#Strings (contain only characters, immutable)
my_string = 'this is a string'

#Lists (ordered sequence of objects, any type)
my_list = [1, 2, "a"]

#Dictionaries (unordered sequence of objects which consist of key:value)
my_dict = {"a":1, "b":2}

#Tuples (ordered sequence of objects, immutable, any type)
my_tuple = (1, 2, "a")

#Sets (unordered sequence of unique keys)
my_set = set([1, 2, 3, 4])

#Frozensets (unordered sequence of unique keys, immutable)
my_frozenset = frozenset([1, 2, 3])

print(my_string)
print(my_list)
print(my_dict)
print(my_tuple)
print(my_set)
print(my_frozenset)
