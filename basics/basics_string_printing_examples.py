### STRING PRINTING EXAMPLE

string1 = 'hello'
string2 = 'world'
print('today I\'ll tell you: {} {}'.format(string1,string2))
print('today I\'ll tell you: {1} {0}'.format(string1,string2))
print('today I\'ll tell you: %s %s' %(string1,string2))
print('today I\'ll tell you: {string3} {string4}'.format(string3='hello',string4='world'))
print(f'today I\'ll tell you: {string1} {string2}')
